package Collection;

// wrapper classes provide a way to use primitive data types as objects
// Autoboxing -> Java supports automatic conversion between primitives and wrapper classes


public class WrapperClasses {

    public static void main(String[] args) {
        // int, float, double, char, long, short, byte
        // int a = 10;

        // Wrapper Classes
        // int -> Integer
        // float -> Float
        // double -> Double
        // char -> Char
        // long -> Long
        // byte -> Byte
        // boolean -> Boolean

        // new
        Integer num = 11; // Autoboxing -> primitive to wrapper class
        int a = num; // unboxing -> wrapper class to primitive

//        System.out.println(num);
//        System.out.println(a);
    }
}
